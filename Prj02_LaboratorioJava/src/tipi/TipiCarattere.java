package tipi;

public class TipiCarattere {

	public static void main(String[] args) {

		char a = 'a';
		System.out.println(a);
		a = 'c'; 	// è possibile cambiarne il valore (in un altro carattere) perché char a è una variabile
		System.out.println(a);
		a = 65;
		System.out.println(a); // stampa "A" perché 65 è il valore corrispondente al carattere "A"
		
	}

}
